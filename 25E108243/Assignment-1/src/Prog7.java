
public class Prog7 {

	public static void main(String[] args) {
		int a,b,c,d;
		a = 50;
		b = 100;
		c = 150;
		System.out.println("Value before swapping");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		System.out.println("c = "+c);
		d = a;
		a = b;
		b = c;
		c = d;
		System.out.println("Value after swapping");
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		System.out.println("c = "+c);
		

	}

}
